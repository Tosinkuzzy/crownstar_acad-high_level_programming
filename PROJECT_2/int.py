# WAP that specify literal values in decimal, binary, octal and hexadecimal forms.
# Notice that when we run our code, the PVM will always provide values only in decimal form. Values in python can be represented in the following ways
# Decimal form (Base-10) - Allowed digits: 0 to 9
# Binary Form (Base-2) - Allowed digits: 0 & 1. Literal values prefixed with: 0b or 0B
# Octal Form (Base-8) - Allowed digits: 0 to 7. Literal values prefixed with 0o or 0O
# Hexa Decimal Form (Base-16) - Allowed digits: 0 to 9, a - f(both upper and lower cases). Literals prefixed with 0x or 0X
a=10
b=0o10
c=0x10
d=0B10
print(a)
print(b)
print(c)
print(d)
