# A program to average two exam scores.

def main():
	print("This program computes the average of two exam scores.")
	fr = eval(input("Enter first score: "))
	sc = eval(input("Enter second score: "))
	average = (fr + sc) / 2
	print("The average of the scores is:", average)
main()
